package week_15;

public class IntegerVariables {

	public static void main(String[] args) {
		int checking;
		byte miles;
		short minutes;
		long days;
		
		checking = -20;
		miles = 105;
		minutes = 120;
		days = 189000;
		System.out.println("We have made a journey of " + miles + " miles");
		System.out.println("took " + minutes + " minutes");
		System.out.println("balance " + checking + "$");
		System.out.println("days " + days);
	}

}
